# Install backend/requirements.txt into scf_site_packages for CloudBase HTTP zip (Python 3.10 on Linux).
# On Windows, uses manylinux2014_x86_64 + cp310 wheels so packages run on SCF Linux (not win_amd64).
# Usage: cd deploy/cloudbase; .\install-scf-site-packages.ps1

$ErrorActionPreference = "Stop"
$here = $PSScriptRoot
$repoRoot = (Resolve-Path (Join-Path $here "..\..")).Path
$req = Join-Path $repoRoot "backend\requirements.txt"
$target = Join-Path $here "scf_site_packages"

if (Test-Path $target) {
    Remove-Item -LiteralPath $target -Recurse -Force
}
New-Item -ItemType Directory -Path $target | Out-Null

if (-not (Get-Command python -ErrorAction SilentlyContinue)) {
    throw "Python not found. Install Python 3.x with pip and add to PATH."
}

$linuxArgs = @(
    "-m", "pip", "install", "-r", $req, "-t", $target,
    "--upgrade", "--no-cache-dir",
    "--platform", "manylinux2014_x86_64",
    "--python-version", "310",
    "--implementation", "cp",
    "--abi", "cp310",
    "--only-binary=:all:"
)

Write-Host "Installing Linux cp310 wheels into scf_site_packages..."
& python @linuxArgs
if ($LASTEXITCODE -ne 0) {
    throw "pip install (manylinux) failed with exit code $LASTEXITCODE"
}
Write-Host ('OK: installed to ' + $target)
